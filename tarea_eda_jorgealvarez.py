# Buscamos DETECTAR los Outliers

from sklearn.neighbors import LocalOutlierFactor

def detectar_outlier_LOF(dataset, columnas, n_neighbors=20, contamination='auto'):
    """
    Detectar outliers usando Local Outlier Factor en múltiples columnas

    Parámetros:
    - dataset: DataFrame
    - columnas: lista de columnas a analizar
    - n_neighbors: número de vecinos a considerar (default 20)
    - contamination: proporción esperada de outliers ('auto' o valor entre 0 y 0.5)

    Retorna:
    - DataFrame con columnas 'LOF_outlier' (1=normal, -1=outlier) y 'LOF_score'
    """
    datos = dataset.copy()

    # Filtrar datos válidos (sin NaN)
    datos_validos = datos[columnas].dropna()

    if len(datos_validos) == 0:
        print("No hay datos válidos para analizar")
        return datos

    # Escalar los datos
    X = datos_validos.values
    X_scaled = StandardScaler().fit_transform(X)

    # Aplicar LOF
    lof = LocalOutlierFactor(n_neighbors=n_neighbors, contamination=contamination)
    etiquetas = lof.fit_predict(X_scaled)  # 1 = normal, -1 = outlier
    scores = lof.negative_outlier_factor_

    # Inicializar columnas con NaN
    datos['LOF_outlier'] = np.nan
    datos['LOF_score'] = np.nan

    # Asignar valores solo a filas válidas
    datos.loc[datos_validos.index, 'LOF_outlier'] = etiquetas
    datos.loc[datos_validos.index, 'LOF_score'] = scores

    # Estadísticas
    n_outliers = (etiquetas == -1).sum()
    n_normales = (etiquetas == 1).sum()
    print(f"Outliers detectados: {n_outliers} ({n_outliers/len(etiquetas)*100:.2f}%)")
    print(f"Puntos normales: {n_normales} ({n_normales/len(etiquetas)*100:.2f}%)")

    return datos

