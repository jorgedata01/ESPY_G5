def filtrar_outliers_isolation_univariante(data, columnas, conservar_columnas_aux=False):
    """
    Filtra (elimina) las filas que contienen outliers en cualquiera de las columnas especificadas

    Parámetros:
    - data: DataFrame con columnas {columna}_outlier
    - columnas: lista de columnas evaluadas
    - conservar_columnas_aux: si True, mantiene las columnas *_outlier y *_score

    Retorna:
    - DataFrame filtrado sin outliers
    """
    df_filtrado = data.copy()

    if isinstance(columnas, str):
        columnas = [columnas]

    # Crear máscara para filtrar
    mascara_outliers = pd.Series([False] * len(df_filtrado), index=df_filtrado.index)

    for col in columnas:
        outlier_col = f"{col}_outlier"

        if outlier_col not in df_filtrado.columns:
            print(f"Advertencia: No existe la columna '{outlier_col}'")
            continue

        # Marcar filas con outliers (-1)
        mascara_outliers = mascara_outliers | (df_filtrado[outlier_col] == -1)

    # Filtrar filas
    n_antes = len(df_filtrado)
    df_filtrado = df_filtrado[~mascara_outliers]
    n_despues = len(df_filtrado)
    n_eliminados = n_antes - n_despues

    print(f"\nFilas eliminadas: {n_eliminados} ({n_eliminados/n_antes*100:.2f}%)")
    print(f"Filas restantes: {n_despues}")

    # Eliminar columnas auxiliares si se solicita
    if not conservar_columnas_aux:
        columnas_aux = [f"{col}_outlier" for col in columnas] + [f"{col}_score" for col in columnas]
        columnas_aux_existentes = [c for c in columnas_aux if c in df_filtrado.columns]
        df_filtrado = df_filtrado.drop(columns=columnas_aux_existentes)
        print(f"Columnas auxiliares eliminadas: {len(columnas_aux_existentes)}")

    return df_filtrado


