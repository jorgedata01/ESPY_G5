# DIAGNÓSTICO: Encontrar epsilon óptimo
def encontrar_epsilon_optimo(dataset, columnas, k=5):
    """
    Encuentra el epsilon óptimo usando el método del codo (k-distance)
    """
    # Preparar datos
    datos_validos = dataset[columnas].dropna()
    X = datos_validos.values
    x_scaled = StandardScaler().fit_transform(X)

    # Calcular distancias a los k vecinos más cercanos
    vecinos = NearestNeighbors(n_neighbors=k)
    vecinos.fit(x_scaled)
    distancias, indices = vecinos.kneighbors(x_scaled)

    # Ordenar distancias al k-ésimo vecino
    distancias_k = np.sort(distancias[:, k-1], axis=0)

    # Graficar
    plt.figure(figsize=(10, 6))
    plt.plot(distancias_k)
    plt.xlabel('Puntos ordenados')
    plt.ylabel(f'Distancia al {k}-ésimo vecino más cercano')
    plt.title('Método del Codo para encontrar Epsilon óptimo')
    plt.grid(True)
    plt.axhline(y=np.median(distancias_k), color='r', linestyle='--', label=f'Mediana: {np.median(distancias_k):.2f}')
    plt.axhline(y=np.percentile(distancias_k, 95), color='g', linestyle='--', label=f'Percentil 95: {np.percentile(distancias_k, 95):.2f}')
    plt.legend()
    plt.show()

    print(f"\nSugerencias de epsilon:")
    print(f"- Epsilon conservador (mediana): {np.median(distancias_k):.2f}")
    print(f"- Epsilon permisivo (percentil 95): {np.percentile(distancias_k, 95):.2f}")

    return np.median(distancias_k), np.percentile(distancias_k, 95)

