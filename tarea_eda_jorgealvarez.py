def detectar_outliers_isolation_univariante(data, columnas, contamination=0.1):
    """
    Detecta outliers usando Isolation Forest para cada columna individualmente

    Parámetros:
    - data: DataFrame
    - columnas: lista de nombres de columnas a analizar
    - contamination: proporción esperada de outliers (default 0.1 = 10%)

    Retorna:
    - DataFrame con columnas adicionales: {columna}_outlier y {columna}_score
    """
    df_result = data.copy()

    if isinstance(columnas, str):
        columnas = [columnas]

    for col in columnas:
        if col not in df_result.columns:
            print(f"Advertencia: La columna '{col}' no existe en el dataset")
            continue

        # Preparar datos (eliminar NaN)
        datos = df_result[[col]].dropna()

        if len(datos) == 0:
            print(f"Advertencia: La columna '{col}' no tiene datos válidos")
            continue

        # Crear y entrenar modelo
        modelo = IsolationForest(contamination=contamination, random_state=42)
        predicciones = modelo.fit_predict(datos)
        scores = modelo.score_samples(datos)

        # Agregar resultados al dataset (inicializar con NaN)
        df_result[f'{col}_outlier'] = np.nan
        df_result[f'{col}_score'] = np.nan

        # Asignar valores solo a las filas sin NaN
        df_result.loc[datos.index, f'{col}_outlier'] = predicciones
        df_result.loc[datos.index, f'{col}_score'] = scores

        # Contar outliers
        n_outliers = (predicciones == -1).sum()
        n_normal = (predicciones == 1).sum()
        print(f"[{col}] Normales: {n_normal}, Outliers: {n_outliers}")

    return df_result


