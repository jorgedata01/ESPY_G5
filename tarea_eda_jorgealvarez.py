# BUscamos DETECTAR los outliers, con el Método DBSCAN

from sklearn.cluster import DBSCAN
from sklearn.preprocessing import StandardScaler

def detectar_outlier_dbscan(dataset, columnas, epsilon=0.5, min_samples=5):
    """
    Detecta outliers utilizando el algoritmo DBSCAN

    Parámetros:
    - dataset: DataFrame
    - columnas: lista de columnas a analizar
    - epsilon: distancia máxima entre puntos (eps)
    - min_samples: número mínimo de puntos para formar un cluster

    Retorna:
    - DataFrame con columna 'cluster' (-1 = outlier, >=0 = cluster)
    """
    df_resultado = dataset.copy()

    # Eliminar filas con NaN en las columnas especificadas
    datos_validos = df_resultado[columnas].dropna()

    # Escalar las variables
    X = datos_validos.values
    x_scaled = StandardScaler().fit_transform(X)

    # Aplicar DBSCAN
    dbscan = DBSCAN(eps=epsilon, min_samples=min_samples).fit(x_scaled)
    etiquetas = dbscan.labels_  # -1 = outlier, 0 o más = cluster

    # Asignar clusters (inicializar con NaN)
    df_resultado['cluster'] = np.nan
    df_resultado.loc[datos_validos.index, 'cluster'] = etiquetas

    # Contar outliers y clusters
    n_outliers = (etiquetas == -1).sum()
    n_clusters = len(set(etiquetas)) - (1 if -1 in etiquetas else 0)
    n_normales = len(etiquetas) - n_outliers

    print(f"Outliers detectados: {n_outliers}")
    print(f"Puntos normales: {n_normales}")
    print(f"Número de clusters: {n_clusters}")

    return df_resultado

